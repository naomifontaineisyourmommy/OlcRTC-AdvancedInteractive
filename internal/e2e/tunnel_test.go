func realE2ECaseExpectation(carrierName, transportName string) realE2EExpectation {
	switch carrierName {
	case "telemost":
		// Telemost SFU is video-only: it strips canPublishData and
		// drops non-media SCTP, so datachannel and seichannel cannot
		// carry tunnel traffic. Video paths (vp8channel, videochannel)
		// must work end-to-end.
		switch transportName {
		case transportVP8, transportVideo:
			return realE2EExpectPass
		default:
			return realE2EExpectFail
		}
	case "wbstream":
		// wbstream guest tokens carry canPublishData=false, so SCTP
		// data channels do not route real bytes - the connection
		// establishes but echoes nothing. We mark this Unstable
		// rather than ExpectFail because a non-guest token (paid /
		// authenticated session) does pass; the matrix should not
		// reward a successful run as "UNEXPECTED SUCCESS" when the
		// upstream side actually fixes their guest flow.
		// Video / SEI / VP8 transports must work in both modes.
		if transportName == transportData {
			return realE2EExpectUnstable
		}
		return realE2EExpectPass
	case "jitsi":
		// Jitsi supports both datachannel and vp8channel for tunnel traffic.
		// VP8 channel provides better reliability when datachannels are
		// constrained or filtered by intermediary middleboxes.
		if transportName == transportData || transportName == transportVP8 {
			return realE2EExpectPass
		}
		return realE2EExpectFail
	default:
		return realE2EExpectPass
	}
}

func realE2EExpectationLabel(expectation realE2EExpectation) string {
	switch expectation {
	case realE2EExpectPass:
		return "SUCCESS"
	case realE2EExpectFail:
		return "EXPECTED FAIL"
	case realE2EExpectUnstable:
		return "UNSTABLE"
	default:
		return "UNKNOWN"
	}
}

// logUnstableOutcome records the result of an Unstable matrix entry
// without failing the test. Unstable combos exist to keep the matrix
// honest about transports that flap against a particular carrier
// (e.g. seichannel against meet.handyweb.org's bandwidth allocator)
// while still surfacing whether the run happened to pass or fail.
func logUnstableOutcome(t *testing.T, label, carrierName, transportName string, err error) {
	t.Helper()
	if err == nil {
		t.Logf("%s PASS %s/%s", label, carrierName, transportName)
		return
	}
	t.Logf("%s FAIL %s/%s: %v", label, carrierName, transportName, err)
}

